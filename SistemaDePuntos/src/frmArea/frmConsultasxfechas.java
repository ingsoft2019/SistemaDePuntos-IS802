    public void buscarCliente() throws SQLException {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);
        resultado = Conexion.Conexion.consulta("SELECT PRC.fecha_registro,PRC.puntos_actuales, FAC.nombre , FAC.sexo \n" +
"FROM [PR].[dbo].[Cliente] PRC  inner join [FA].[dbo].[CLI_CLI] FAC on FAC.[id] = PRC.id_cliente \n" +
"where  PRC.estado ='A' and PRC.fecha_registro BETWEEN  '"+txtfechainicio.getText()+"' and  '"+txtfechafin.getText()+"';");
        try {
            while (resultado.next()) {
                Vector v = new Vector();
                v.add(resultado.getString(1));
                System.out.println("El Valor es: "+resultado.getString(1));
                v.add(resultado.getString(2));
                v.add(resultado.getString(3));
                v.add(resultado.getString(4));
                modelo.addRow(v);
                jTable1.setModel(modelo);
            }
        } catch (SQLException e) {
           throw e;     
        }catch (Exception ex) {
           throw ex;     
        }
    }
