    public void cargarZonas(String zona) {
        ResultSet zonas = Zona.mostrarZonas();
        //LLenamos nuestro ComboBox
        jC_zona.addItem(zona);
        try {
            while (zonas.next()) {
                if (!zonas.getString("zona").equals(zona)) {
                    jC_zona.addItem(zonas.getString("zona"));
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(mdl_SubMenuCliente.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    //Metodo para llenar los campos     
    public void llenarCampos(String pnombre, String snombre, String papellido, String sapellido, String identidad,
            String sexo, String telefono1, String telefono2, String telefono3, String correo,
            Date fechaNac, String detalleDireccion, String puntos_actuales, String puntos_rifa,
            String fechaVencimiento, String id, String estado) {
        this.getTxt_primerNombre().setText(pnombre);
        this.getTxt_segundoNombre().setText(snombre);
        this.getTxt_primerApellido().setText(papellido);
        this.getTxt_segundoApellido().setText(sapellido);
        this.getTxt_identidad().setText(identidad);
        if ("M".equals(sexo)) {
            this.jRadioButtonMasculino.setSelected(true);
        } else {
            this.jRadioButtonFemenino.setSelected(true);
        }
        this.getTxt_telefono1().setText(telefono1);
        this.getTxt_telefono2().setText(telefono2);
        this.getTxt_telefono3().setText(telefono3);
        this.getTxt_email().setText(correo);
        this.jD_fechaNac.setDate(fechaNac);
        this.getTxt_datalleDireccion().setText(detalleDireccion);
        this.getTxt_puntosRegis().setText(puntos_actuales);
        this.getTxt_puntoRifa().setText(puntos_rifa);
        this.getTxt_vencimiento().setText(fechaVencimiento);
        this.getId().setText(id);
        this.estado = estado;

        if ("A".equals(estado)) {
            btn_habilitarcliente.setVisible(false);
            btn_desactivarCliente.setVisible(true);
        } else {
            btn_habilitarcliente.setVisible(true);
            btn_desactivarCliente.setVisible(false);
        }

    }

