package Chats.SistemasVentasPuntos;

public class ChartsCircularResponse {
    private String nombrecompleto;
    private String puntaje;
    private String fechaNacimirnto;

    /**
     * @return the nombrecompleto
     */
    public String getNombrecompleto() {
        return nombrecompleto;
    }

    /**
     * @param nombrecompleto the nombrecompleto to set
     */
    public void setNombrecompleto(String nombrecompleto) {
        this.nombrecompleto = nombrecompleto;
    }

    /**
     * @return the puntaje
     */
    public String getPuntaje() {
        return puntaje;
    }

    /**
     * @param puntaje the puntaje to set
     */
    public void setPuntaje(String puntaje) {
        this.puntaje = puntaje;
    }

    /**
     * @return the fechaNacimirnto
     */
    public String getFechaNacimirnto() {
        return fechaNacimirnto;
    }

    /**
     * @param fechaNacimirnto the fechaNacimirnto to set
     */
    public void setFechaNacimirnto(String fechaNacimirnto) {
        this.fechaNacimirnto = fechaNacimirnto;
    }

}

